    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        chooser.showOpenDialog(null);
        f = chooser.getSelectedFile();
        jTextField1.setText(f.getName());
        
        ImageIcon imageIcon = new ImageIcon(f.getPath());
        if(imageIcon.getIconHeight() > 100 || imageIcon.getIconWidth() > 400){
            Image image = imageIcon.getImage();
            Image newimg = image.getScaledInstance(100, 100, java.awt.Image.SCALE_SMOOTH);
            ImageIcon icon = new ImageIcon(newimg);
            jLabel1.setIcon(icon); 
        }
        else
            jLabel1.setIcon(imageIcon);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String NomeDoArquivo = f.getName();
        File destino = new File(".\\" + NomeDoArquivo);
        System.out.println("" + destino.toString());
        destino = destino.getAbsoluteFile();

        try {
            Files.copy(f.toPath(), destino.toPath());
            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
        } catch (IOException ex) {
            
            JOptionPane.showMessageDialog(null, "Erro! " + ex);
        } 
 
    }//GEN-LAST:event_jButton2ActionPerformed

